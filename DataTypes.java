public class DataTypes {
    public static void main(String[] args) {
        int num = 25 ;
        char ch = 'a';
        float f = 2.3f;
        double d= 4.865947;
        System.out.println("Number :"+num);
        System.out.println("char :"+ch);
        System.out.println("Float :"+f);
        System.out.println("double :"+d);
    }
}
